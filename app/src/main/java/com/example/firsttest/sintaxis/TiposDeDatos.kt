package com.example.firsttest.sintaxis

fun main(){

    // String -> Texto
    // Int -> Numeros Enteros
    // Double -> Numeros con decimales
    // Float -> Numeros con decimales, con menos digitos
    // Boolean -> Verdadero o falso

    var myString = "Esto es un texto"
    var myInt = 10

    var myDouble = 10.5
    var myFloat = 10.5f

    var myBoolean = true
    var mySecondBoolean = false

    println(myString)
    println(myInt)
    println(myDouble)
    println(myFloat)
    println(myBoolean)
    println(mySecondBoolean)
}