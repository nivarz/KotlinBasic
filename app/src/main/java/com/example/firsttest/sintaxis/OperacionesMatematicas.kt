package com.example.firsttest.sintaxis

fun main() {

    val num1= 10
    val num2 = 3

    val suma = num1 + num2
    println(suma)

    val resta = num1 - num2
    println(resta)

    val multiplicacion = (num1 * num2) + 5
    println(multiplicacion)

    val division = num1 / num2 // Esta es el operador que me trae en el caso de la division el entero
    println(division)

    val modulo = num1 % num2 // Esta es el operador que me trae en el caso de la division el restante o decimal
    println(modulo)
}