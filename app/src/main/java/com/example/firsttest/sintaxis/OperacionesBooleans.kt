package com.example.firsttest.sintaxis

fun main(){

    val num1 = 5
    val num2 = 10

    val isEqual = num1 == num2 // El igual simple es para asignar valores, el doble es para preguntar
    println(isEqual)

    val isNotEqual = num1 != num2
    println(isNotEqual)

    val isGreater = num1 > num2
    println(isGreater)

    val isLess = num1 < num2
    println(isLess)
}
